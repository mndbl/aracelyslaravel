        <section id="sop_305">
            <h2 class="text-base text-gray-600 font-semibold tracking-wide uppercase">
                SOP's. The 305 total experience:
            </h2>
            <h2 class="text-base mt-4 text-gray-600 font-semibold tracking-wide capitalize">
                Employee Health and Personal Hygiene
            </h2>
            <p class="mx-auto max-w-2xl text-md text-gray-500">
                Policy: All foodservice employees will communicate health concerns and maintain
                good personal hygiene practices to safety of food.
                Procedure: All restaurant employees must:
            </p>
            <ul class="mt-4 mx-auto max-w-2xl text-md text-gray-500">
                <p>Grooming:</p>

                <li>1. Arrive at work clean with clean hair, clean clothing, teeth brushed, bathed and daily use of deodorant. </li>
                <li>2. Maintain short, clean, and polish-free fingernails. No artificial nails are permitted in the food production area.</li>
                <li>
                    3. Wash hands (including under fingernails) and up to forearms vigorously and thoroughly with soap and warm water for a period of 20 seconds, following Handwashing SOP:
                    <ul class="ml-4">
                        <li>• When entering the facility before work begins.</li>
                        <li>• Immediately before preparing food or handling equipment.</li>
                        <li>• As often as necessary during food preparation when contamination occurs.</li>
                        <li>• Before putting on gloves to start a task that involves working with food.</li>
                        <li>• In the restroom after toilet use and when you return to your work station.</li>
                        <li>• When switching between working with raw foods and ready-to-eat or cooked foods.</li>
                        <li>• After touching face, nose, hair, or any other body part, and after sneezing or coughing.</li>
                        <li>• After cleaning duties.</li>
                        <li>• Between each task performed and before wearing disposable gloves.</li>
                        <li>• After smoking, eating, or drinking.</li>
                    </ul>
                </li>

                <li>4. Wash hands only in hand sinks designated for that purpose.</li>
                <li>5. Dry hands with single use towels or forced air dryer. Turn off faucets using a paper towel, in order to prevent re contamination of clean hands.</li>
            </ul>
            <ul class="mt-4 mx-auto max-w-2xl text-md text-gray-500">
                <p>Fingernails:</p>
                <li>
                    1. Fingernails should be trimmed, filed, and maintained so edges and surfaces are
                    cleanable and not rough.
                </li>
            </ul>
            <ul class="mt-4 mx-auto max-w-2xl text-md text-gray-500">
                <p>Proper Attire:</p>

                <li>1. Wear appropriate clothing – clean uniform with sleeves and clean non-skid close-toed work shoes (or leather tennis shoes) that are comfortable for standing and working on floors that can be slippery.</li>
                <li>2. Wear apron on site, as appropriate.
                <ul class="ml-4">
                    <li>• Do not wear apron to and from work.</li>
                    <li>• Take off apron before using the restroom.</li>
                    <li>• Remove apron when leaving the foodservice area.</li>
                    <li>• Change apron if it becomes soiled or stained.</li>
                    <li>• Follow Glove Use SOP. </li>
                    <li>• Only gloves approved for foodservices should be used.</li>
                    <li>• Wear provided, disposable gloves to cover  any cuts, sores, rashes, or lesions. </li>
                    <li>• Wear gloves when handling ready-to-eat foods that will not be heated-treated. Gloves should be worn or utensils used when serving food. </li>
                    <li>• Change disposable gloves between tasks or every four hours if uninterrupted work on continuous task has occured. Note that opening a refrigerator door to place pan of prepared product is considered a new task.   Wash hands before donning and after discarding gloves. Do not blow into gloves, roll gloves, or wash and reuse gloves.</li>
                </ul>
                </li>
            </ul>

            <ul class="mt-4 mx-auto max-w-2xl text-md text-gray-500">
                <p>Hair Restraints and Jewelry: </p>
                <li>1 Wear a hair net or bonnet in any food production area so that all hair is completely covered.</li>
                <li>2. Front-of-house staff must wear a hair restraint that keeps hands out of hair and hair out of food. </li>
                <li>3. Keep beards and mustaches (only allowed for front of house staff) neat and trimmed. Facial hair (beard) restraints are required in any food production area. Note, restaurants can impose greater rigor and ban any facial hair. </li>
                <li>4. Do not wear false eyelashes. Contamination of foods.  </li>
                    <ul>
                        <li>• Only a plain wedding band.</li>
                        <li>• No necklaces, bracelets, or dangling jewelry are permitted.</li>
                        <li>• No earrings or piercings that can be removed are permitted.</li>
                    </ul>
            </ul>
            <ul class="mt-4 mx-auto max-w-2xl text-md text-gray-500">
                <p>Illness: </p>

                <li>
                    1. Report any flu-like symptoms, diarrhea, jaundice, sore throat with fever,
                    uncovered open wounds with fluid, boils, and/or vomiting to the unit supervisor. 
                     Employees with these symptoms will be sent home with the exception of symptoms
                    from a noninfectious condition. ; Employees could be re-assigned to activities so
                    that there is no risk of transmitting a disease through food. 
                </li>
                <li>
                    2. Instances of illness or recent exposure to Norovirus, Hepatitis A, Nontyphodial
                    Salmonella, Salmonella Typhi, Shigella, or Shiga Toxin-producing Escherichia Coli
                    must be reported to the unit supervisor. Exclude the foodservice employee if
                    diagnosed with an infection from Norovirus, Hepatitis A, Nontyphodial Salmonella,
                    Salmonella Typhi, Shigella, or Shiga Toxin-producing Escherichia Coli.
                </li>
            </ul>

            <ul class="mt-4 mx-auto max-w-2xl text-md text-gray-500">
                <p>Cuts, Abrasions, and Burns:</p>
                <li>1. Bandage any cut, abrasion, or burn that has broken the skin.</li>
                <li>2. Cover bandages on hands with gloves and finger cots, and change as
                appropriate.</li>
                <li>3. Inform unit supervisor of all wounds.</li>
            </ul>
            <ul class="mt-4 mx-auto max-w-2xl text-md text-gray-500">
                <p>Smoking, eating, and gum chewing:</p>
                <li>1. Smoke, eat and chew gum only in designated areas. No smoking or chewing tobacco shall occur inside production facilities.</li>
                <li>2. A closed beverage container with a straw or a sip-lid top may be used in the production area if the container is handled to prevent contamination of 1) the employee’s hands, 2) the container, and 3) exposed food, clean equipment, utensils, linens, and unwrapped single-service and single-use articles.</li>
                <li>3. Refrain from chewing gum or eating candy during work in a food production area.</li>
            </ul>
            <ul class="mt-4 mx-auto max-w-2xl text-md text-gray-500">
                <p>
                    The restaurant manager will:
                </p>
                <li>1. Monitor to ensure employees are following proper hygiene requirements when they report to work.</li>
                <li>2. Maintain signed statements from employees agreeing to report illness. </li>
                <li>3. Post signs reminding staff to notify managers when sick.</li>
                <li>4. Work with local regulatory agency to monitor reportable illnesses.</li>
                <li>5. Follow up as necessary.</li>
            </ul>
            <h2 class="text-base mt-4 text-gray-600 font-semibold tracking-wide capitalize">
                Certified Food Protection Manager
            </h2>
            <p class="mx-auto max-w-2xl text-md text-gray-500">
                Policy:  Food Code 2009 with 2011 Supplement requires at least one foodservice
                employee with management and supervisory responsibility be a Certified Food
                Protection Manger (CFPM).  The CFPM does not need to be on site during all hours
                of operation.
            </p>
            <ul class="mt-4 mx-auto max-w-2xl text-md text-gray-500">
                <p>
                    Procedure:  Criteria for a CFPM are the following:
                </p>
                <li>1. Can identify day-to-day hazards in a foodservice operation.</li>
                <li>2. Develops or implements specific policies, procedures, or standard aimed at preventing foodborne illness.</li>
                <li>3. Coordinates training, supervision, or direction of food preparation activities, implements corrective action as needed to protect health of students and staff.</li>
                <li>4. Conducts periodic in-house self-inspections of daily operations to verify all policies and procedures concerning food safety are being follows.</li>
            </ul>
            <ul class="mt-4 mx-auto max-w-2xl text-md text-gray-500">
                <p>
                 Guindance:
                </p>
                <li>1. After January 2014, any new foodservice operation must have a CFPM on staff within six months of opening.</li>
                <li>2. Older establishments which have received a violation which could contribute to foodborne illness must have a CFPM on staff within six months of receiving the violation.</li>
                <li>3. Older establishments with no violations which could contribute to foodborne illness must have a CFPM on staff by January 1, 2018.</li>
                <li>4. Check with your local health inspector to determine schedule for your operation.</li>
            </ul>
            <ul class="mt-4 mx-auto max-w-2xl text-md text-gray-500">
                <p>
                    Training:
                </p>
                <li>1. The Conference on Food Protection has identified programs that meet the CFPM designation. </li>
                <li>2. Food safety training to become a CFPM are available from several organizations Iowa State University Extension and Outreach partners with the Iowa Restaurant Association to offer the National Restaurant Association’s ServSafe® Program. This certification is good for five years. Some foodservice distribution company offer the training for their customers.</li>
            </ul>
            <ul class="mt-4 mx-auto max-w-2xl text-md text-gray-500">
                <p>
                    The restaurant manager will ensure this requirement is met.
                </p>
                <li>1. Monitor to ensure food safety policies and procedures are followed. </li>
                <li>2. Provide food safety training for the certified food protection manager.</li>
            </ul>
